#include "3-calc.h"

/**
 * op_add - returns sum of two numbers
 * @a: number 1
 * @b: number 2
 *
 * Return: result
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - returns difference of two numbers
 * @a: number 1
 * @b: number 2
 *
 * Return: result
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - returns multiplication of two numbers
 * @a: number 1
 * @b: number 2
 *
 * Return: result
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - returns division of two numbers
 * @a: number 1
 * @b: number 2
 *
 * Return: result
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - returns remainder of division of two numbers
 * @a: number 1
 * @b: number 2
 *
 * Return: result
 */

int op_mod(int a, int b)
{
	return (a % b);
}
